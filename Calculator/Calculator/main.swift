import Foundation

func userGreeting(message: String) -> String {
    print(message)
    let input: String = readLine()!
    if input == "" {
        return userGreeting(message: "I didnt quite catch that, try again?")
    } else {
        return input
    }
}

func mathStuffFactory(opString: String) -> (Double, Double) -> Double {
  switch opString {
  case "+":
    return {x, y in x + y }
  case "-":
    return {x, y in x - y }
  case "*":
    return {x, y in x * y }
  case "/":
    return {x, y in x / y }
  default:
    return {x, y in x + y }
  }
}
